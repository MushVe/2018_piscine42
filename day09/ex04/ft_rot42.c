char	*ft_rot42(char *str)
{
	int i;
	int cd;

	i = 0;
	cd = 0;
	while (str[i])
	{
		if ((str[i] > 96 && str[i] < 123)
			|| (str[i] > 64 && str[i] < 91))
		{
			while (cd < 42)
			{
				str[i] = str[i] + 1;
				if (str[i] == 91 || str[i] == 123)
					str[i] = str[i] - 26;
				cd++;
			}
			cd = 0;
		}
		i++;
	}
	return (str);
}
