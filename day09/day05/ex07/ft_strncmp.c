int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (s1[i] == s2[i] && n > i)
	{
		i++;
		if ((!s1[i] && !s2[i]) || n == i)
			return (0);
	}
	return (s1[i] - s2[i]);
}
