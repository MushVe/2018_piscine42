int	ft_antidote(int i, int j, int k)
{
	if ((j < i && i < k)
		|| (j > i && i > k))
		return (i);
	if ((i < j && j < k)
		|| (i > j && j > k))
		return (j);
	if ((j < k && k < i)
		|| (j > k && k > i))
		return (k);
	return (-1);
}
